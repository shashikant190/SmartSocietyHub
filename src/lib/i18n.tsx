interface I18nContextValue {
  language: AppLanguage;
  setLanguage: (language: AppLanguage) => void;
  t: (key: string) => string;
}

const I18nContext = createContext<I18nContextValue | null>(null);

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguageState] = useState<AppLanguage>("en");

  useEffect(() => {
    const saved = localStorage.getItem("society-language") as AppLanguage | null;
    if (saved && LANGUAGE_OPTIONS.some((option) => option.code === saved)) {
      setLanguageState(saved);
      document.documentElement.lang = saved === "hi" ? "hi-IN" : saved === "mr" ? "mr-IN" : "en";
    }
  }, []);

  const setLanguage = (nextLanguage: AppLanguage) => {
    setLanguageState(nextLanguage);
    localStorage.setItem("society-language", nextLanguage);
    document.documentElement.lang = nextLanguage === "hi" ? "hi-IN" : nextLanguage === "mr" ? "mr-IN" : "en";
  };

  const value = useMemo<I18nContextValue>(() => ({
    language,
    setLanguage,
    t: (key: string) => dictionary[language][key] || key,
  }), [language]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error("useI18n must be used inside I18nProvider");
  }
  return context;
}

export function translateStaticText(key: string, language: AppLanguage) {
  return dictionary[language][key] || key;
}
